package com.LinkedList;

/**
 *
 * @author Matheus Mendes
 */
public class AlgumaCoisaConcreta {
    
    private int umInteiro;
    private String umaString;

    public AlgumaCoisaConcreta(int umInteiro, String umaString) {
        this.umInteiro = umInteiro;
        this.umaString = umaString;
    }
    
    

    public int getUmInteiro() {
        return umInteiro;
    }

    public void setUmInteiro(int umInteiro) {
        this.umInteiro = umInteiro;
    }

    public String getUmaString() {
        return umaString;
    }

    public void setUmaString(String umaString) {
        this.umaString = umaString;
    }
    
    
    
}
